<h1>Recent Moves: About</h1>

<p><i>Recent Moves</i> reviews recent moves into the main article space from other namespaces on the English Wikipedia. This kind of article creation is often overlooked by new page patrollers, as they do not appear on <a href="http://en.wikipedia.org/wiki/Special:Newpages">Special:Newpages</a>. There is a <a href="https://bugzilla.wikimedia.org/show_bug.cgi?id=12363">proposed change to Mediawiki</a> to fix this (please vote for it!), and this tool is a stopgap measure in the meantime.</p>

<p>If you have any questions or suggestions about Recent Moves, please contact its author Derrick Coetzee at <a href="http://en.wikipedia.org/wiki/User_talk:Dcoetzee">his talk page on English Wikipedia</a>.</p>

